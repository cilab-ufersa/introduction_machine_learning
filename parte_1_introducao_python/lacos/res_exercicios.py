# Escreva um programa que imprima os números de 1 a 10.

numeros = range(1, 11)

for numero in numeros:
    print(numero)


# Escreva um programa que imprima os números de 10 a 1.

numeros = range(10, 0, -1)

for numero in numeros:
    print(numero)


# Escreva um programa que imprima os números pares de 1 a 10.

numeros = range(1, 11)

for numero in numeros:
    if numero % 2 == 0:
        print(numero)


# Escreva um programa que imprima os números ímpares de 1 a 10.

numeros = range(1, 11)

for numero in numeros:
    if numero % 2 != 0:
        print(numero)








